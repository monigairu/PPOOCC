def write_f3_sheet(ws, title, data):
    """F3シートを書き込む"""
    # タイトル行（row 1）
    ws.cell(row=1, column=1, value=title)
    # 基本情報行（row 3）
    ws.cell(row=3, column=1, value="基本情報")
    ws.cell(row=3, column=2, value="電力会社")
    ws.cell(row=3, column=3, value="（各行参照）")
    # ヘッダー行（row 6）
    _write_row(ws, 6, F3_HEADERS)
    # データ行（row 7〜）
    for i, row_data in enumerate(data, start=7):
        _write_row(ws, i, row_data)


def write_f2_sheet(ws, title, data):
    """F2シートを書き込む"""
    ws.cell(row=1, column=1, value=title)
    ws.cell(row=2, column=1, value="基本情報")
    _write_row(ws, 6, F2_HEADERS)
    for i, row_data in enumerate(data, start=7):
        _write_row(ws, i, row_data)


