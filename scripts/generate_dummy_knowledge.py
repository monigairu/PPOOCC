def _write_row(ws, row_idx, values, fill=None):
    for col_idx, val in enumerate(values, start=1):
        cell = ws.cell(row=row_idx, column=col_idx, value=val)
        if fill:
            cell.fill = fill
        if row_idx == 6:  # ヘッダー行
            cell.font = HEADER_FONT
            cell.alignment = Alignment(wrap_text=True)


